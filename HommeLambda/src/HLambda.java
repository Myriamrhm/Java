
public class HLambda {
	
	//Constructeur 
	public HLambda(String nom, int age, String tel) {
		super();
		this.nom = nom;
		this.age = age;
		this.tel = tel;
	}
	
	
	public HLambda() {
		super();
	}


	// Attributs de Monsieur Lambda 
	private String nom;
	private int age;
	private String tel;
	
	
	// Getters et Setters de l'age 
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
	

}
